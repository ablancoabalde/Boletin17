
package boletin17ejer4;

public class LetraDNI {
    private String letra;
    private int num;

    public LetraDNI(String letra, int num) {
        this.letra=letra;
        this.num=num;
    }

    public int getNum() {
        return num;
    }

    public String getLetra() {
        return letra;
    }
    
    
}
